                    <date class="article__date">5/16/2020</date>
                    <h3 class="article__title">Seeds and science, a marriage made in heaven!</h3>
                    <p class="article__txt">I must say how difficult it’s been germinating tomatoes this season. I live in California’s high desert, where it’s routinely 19-25 degrees at night and 62-68 degrees by 12:00 pm. Add a high tunnel to the equation, and the temperature during the day inside the high tunnel will easily reach 108 degrees.</p>
                    <p class="article__txt">As it turns out, seeds hate these wild swings in temperature and humidity levels.</p>
                    <p class="article__txt">So what can you do to control the variables? And just what are the potential variables that are in play?</p>
                    <p class="article__txt">Let’s first isolate the inputs.</p>
                    <ol class="list">
                        <li class="list__item">Seeds</li>
                        <li class="list__item">Water</li>
                        <li class="list__item">Seed starting mix</li>
                        <li class="list__item">Temperature</li>
                        <li class="list__item">Humidity</li>
                        <li class="list__item">Light levels</li>
                    </ol>
                    <p class="article__txt">Since my other seed starts were started with no controls over temperature, humidity, and light levels, and given that I was using a seed starting mix that worked well for other varieties, and lastly, my seeds were new and were from two well known commercial seed growers, I started thinking that I would eliminate as many variables as possible all at once.</p>

                    <p class="article__txt">
                    <strong>Here’s what I did.</strong><br />
                    <ul class="list">
                        <li class="list__item"> I added 10% hydrogen peroxide to my water.</li>
                        <li class="list__item">I placed the seeds in a paper towel, then moistened the towel and seeds with the H2O Peroxide mixture.</li>
                        <li class="list__item"> I then placed the paper towels inside a plastic storage bag that I left open for ventilation.</li>
                        <li class="list__item">Lastly, I placed the storage bags inside a cardboard box, closed the lid, and placed it above the refrigerator to maintain a constant temperature and humidity.</li>
                    </ul>
                    </p>
                    <p class="article__txt">Frankly, the fun part of science is getting a result from your experiment that varies widely from your initial theory/hypothesis.</p>
                    <p class="article__txt">Here’s the result: My first tomato sprouts of the year, even with a light dusting of snow last night.</p>
                    <p class="article__txt">
                    <strong>The bottom line?</strong><br />
                    Learning which variables impact the problem your trying to solve is invaluable. Isolating those variables to test your hypothesis is even more priceless.
                    </p>
                    <p class="article__txt">Seeds and science, a marriage made in heaven!</p>
