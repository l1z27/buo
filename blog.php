                    <h3 class="article__title">You’ll learn a lot on our daily journey of continuous improvement!</h3>
                    <p class="article__txt">The Berkeley Composting Method is based on good science. We’ve used that knowledge and decided to improve our own composting method to improve soil quality, plant yield, and seed germination.</p>

                    <p class="article__txt">The key is in maintaining discipline to the process and understanding when to do what.</p>

                    <p class="article__txt">Here’s our method and some pictures to go along with it.</p>

                    <p class="article__txt">We begin by gathering horse, chicken, goat, goose, pig, and duck manure. But if you only have horses or one if the other animals, that’s fine too.</p>

                    <p class="article__txt">In step 2 we let the manure dry for a week. This makes it easier to go through step 3—the shredder.</p>

                    <p class="article__txt">In step 3 we add one shovel of dried leaves for every ten shovels of manures, add water and worm tea, and then run it through our chipper shredder. This gives us a carbon to nitrogen ratio of about 30:1—an ideal ratio for composting. It also improves the surface area of the compost, allowing it to break down much faster.</p>

                    <p class="article__txt">In step 4 we let the pile sit, in our case a 27x27x30” soil saver bin, heat up for four days.</p>

                    <p class="article__txt">In step 5 on the 4th day we’ll turn the pile into an empty 27x27x30” soil saver bin that’s placed right next to the existing compost. This allows us to save the extra step of loading and unloading, while keeping the pile in a nice organized spot. In addition, each of the two bins will be right next to the 4x4 beds the compost is ultimately going into—saving us hours of transporting and moving compost around.</p>

                    <p class="article__txt">Step 5 require we turn the compost from one bin to the other every second day until day 17.</p>

                    <p class="article__txt">After 17 days, in Step 6 we place the compost on the ground in a 4x4 template at a rate of 1 1/2” per bed. Each 27x27x30 Soil Saver Bin will produce (5) 4x4 beds 1 1/2” deep.</p>

                    <p class="article__txt">In step 7, we’ll place a basket of worms in composted horse manure on top of each the 4x4 beds after day 20. This does two things to vastly improve the Berkeley Method: 1. It allows red wigglers to turn the compost into vermicompost and beautiful soil while you’re sitting comfortably in your house! 2. In 60 days you’ll have worms and worm castings in the basket to use in any new 4x4 beds you’re creating.</p>

                    <p class="article__txt">In step 8 you’ll start your seedlings at the same Day to Harvest rate which your new 4x4 beds will be ready. In just 60 days you’ll have beautiful soil, happy plants, and knowledge you can share with others!</p>

                    <p class="article__txt">Follow us on Facebook. You’ll learn a lot on our daily journey of continuous improvement!</p>
